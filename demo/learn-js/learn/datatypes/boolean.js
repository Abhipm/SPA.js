/* 
 * Boolean: Primitive DataType
 * true (lower-case) or false (lower-case)
 * 
 * true:
 * >> any number other than 0 (ie. any +ve or -ve numbers)
 * >> any string other than empty ""
 * 
 * false:
 * >> number zero
 * >> empty string
 * >> undefined
 * >> null
 * 
 * use Boolean(value) >> to convert a value to boolean
 * or shotcut !!value
 * 
 * Boolean("Some String") ==> true
 * Boolean("")            ==> false
 * Boolean(" ")           ==> true
 * Boolean(-10)           ==> true
 * Boolean([])            ==> true
 * Boolean({})            ==> true
 *  
 */